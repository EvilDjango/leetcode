public class Topic017 {
    private static Map<Character, List<Character>> map = new HashMap<>();

    static {
        map.put('2', Arrays.asList('a', 'b', 'c'));
        map.put('3', Arrays.asList('d', 'e', 'f'));
        map.put('4', Arrays.asList('g', 'h', 'i'));
        map.put('5', Arrays.asList('j', 'k', 'l'));
        map.put('6', Arrays.asList('m', 'n', 'o'));
        map.put('7', Arrays.asList('p', 'q', 'r', 's'));
        map.put('8', Arrays.asList('t', 'u', 'v'));
        map.put('9', Arrays.asList('w', 'x', 'y', 'z'));
    }

    public List<String> letterCombinations(String digits) {
        List<String> ret = new ArrayList<>((int) Math.pow(3, digits.length()));
        if (digits.isEmpty()) {
            return ret;
        }
        dfs(ret, digits, "", 0);
        return ret;
    }

    /**
     * 深度遍历
     *
     * @param ret
     * @param digits
     * @param i
     */
    private void dfs(List<String> ret, String digits, String temp, int i) {
        if (i == digits.length()) {
            ret.add(temp);
            return;
        }
        for (char c : map.get(digits.charAt(i))) {
            dfs(ret, digits, temp + c, i + 1);
        }
    }
}
